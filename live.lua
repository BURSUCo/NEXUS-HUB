end

--[=[ Cleanup System ]=]

function Library:Unload()
    for _, connection in pairs(Library.Connections) do
        connection:Disconnect()
    end
    for _, animation in pairs(Library.Animations) do
        animation:Cancel()
    end
    for _, element in pairs(Library.Elements) do
        if element.Destroy then
            element:Destroy()
        end
    end
    Library.Elements = {}
    Library.Animations = {}
    Library.Connections = {}
    Library.Registry = {}
    Library.Flags = {}
end

